public class Employee {
    String fullName;
    String position;
    String email;
    String phone;
    int age;

    public Employee(String fullName, String position, String email, String phone, int age) {
        this.fullName = fullName;
        this.position = position;
        this.email = email;
        this.phone = phone;
        this.age = age;
    }
}
